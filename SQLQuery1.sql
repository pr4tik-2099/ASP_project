create table books(
	book_id varchar(10) Primary key,
	book_Name varchar(MAX),
	author_Name varchar(MAX),
	publisher_Name varchar(MAX),
	publish_Date varchar(50),
	book_language varchar(50),
	edition varchar(10),
	book_Cost varchar(10),
	no_of_pages varchar(10),
	book_Description varchar(MAX),
	actual_Stock varchar(10),
	current_Stock varchar(10),
	book_img_link nvarchar(MAX)
);


